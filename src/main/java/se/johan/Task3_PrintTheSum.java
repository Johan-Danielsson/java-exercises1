package se.johan;

public class Task3_PrintTheSum {

    public static void main(String[] args) {
        //Print additions, change add1 and add2 values to add them
        int add1 = 45;
        int add2 = 11;
        int addSum = add1 + add2;
        System.out.println(add1+ "+" +add2+ "=" + addSum);

        //Print multiplications, change multi1 and multi2 values
        // to add them
        int multi1 = 12;
        int multi2 = 4;
        int multiSum = multi1 * multi2;
        System.out.println(multi1+ "*" +multi2+ "=" + multiSum);

        //Print division, change div1 and div2 values to add them
        int div1 = 24;
        int div2 = 6 ;
        int divSum = div1 / div2;
        System.out.println(div1+ "/" +div2+ "=" + divSum);

        //Print subtraction, change sub1 and sub2 values to add them
        int sub1 = 55;
        int sub2 = 12;
        int subSum = sub1 - sub2;
        System.out.println(sub1+ "-" +sub2+ "=" + subSum);
    }
}

